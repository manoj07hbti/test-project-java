package inheritance;

public class A {


    public void m1(){

        System.out.println("I am in M1 method of CLASS A..");
    }

    public void m2(){

        System.out.println("I am in M2 method of CLASS A..");
    }

    public static void main(String[] args) {
        A obj=new A();
        obj.m1();
    }
}
